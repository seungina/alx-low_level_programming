#include "3-calc.h"

/**
 * op_add - add integers
 * @a: int a
 * @b: int b
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub integers
 * @a: int a
 * @b: int b
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul integers
 * @a: int a
 * @b: int b
 * Return: Result
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - div integers
 * @a: int a
 * @b: int b
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod integers
 * @a: int a
 * @b: int b
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
